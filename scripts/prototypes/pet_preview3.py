import sys

def render(pixels, palette):
    rows = len(pixels)
    cols = len(pixels[0])
    out = ""
    for y in range(0, rows - 1, 2):
        for x in range(cols):
            t = pixels[y][x]
            b = pixels[y+1][x]
            tc = palette.get(t)
            bc = palette.get(b)
            if t == 0 and b == 0:
                out += " "
            elif t == 0:
                out += f"\033[48;2;{bc[0]};{bc[1]};{bc[2]}m\033[38;2;0;0;0m▄\033[0m"
            elif b == 0:
                out += f"\033[48;2;0;0;0m\033[38;2;{tc[0]};{tc[1]};{tc[2]}m▀\033[0m"
            else:
                out += f"\033[48;2;{bc[0]};{bc[1]};{bc[2]}m\033[38;2;{tc[0]};{tc[1]};{tc[2]}m▀\033[0m"
        out += "\n"
    return out

